#Функция range используется для того, чтобы
#обозначить в каком промежутке будут выводится числа
#в цикле for
#
#Либо просто для выведения чисел в каком-либо порядке
#
#s:
#1.range(stop) - использует числа от 0 до stop
#2.range(start, stop) - использует числа в промежутке от start до stop
#3.range(start, stop, step) - использует числа в промежутке от start до stop через определенные шаги
# 
# 
#
#
range(5) #0,1,2,3,4
range(7, 12) #7,8,9,10,11
range(0,11,2) #0,2,4,6,8,10